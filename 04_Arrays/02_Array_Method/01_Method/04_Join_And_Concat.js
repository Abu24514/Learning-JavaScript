// ===============================
// 7. join()
// ===============================
// join() converts array into a string

let nums = [1, 2, 3];
console.log(nums.join("-")); // "1-2-3"

// ===============================
// 8. concat()
// ===============================
// concat() joins two arrays and returns a new array

let a = [1, 2];
let b = [3, 4];

let c = a.concat(b);

console.log(c); // [1, 2, 3, 4]
