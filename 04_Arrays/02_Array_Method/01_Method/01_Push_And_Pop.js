// ===============================
// 1. push()
// ===============================
// push() adds a new value at the END of the array

let fruits = ["apple", "banana"];
fruits.push("mango");

console.log(fruits); 
// ["apple", "banana", "mango"]

// ===============================
// 2. pop()
// ===============================
// pop() removes the LAST value from the array
let foods = ["Rice" , "Milk" , "bread"]
foods.pop();

console.log(foods); 
// ["Rice" , "Milk"]
