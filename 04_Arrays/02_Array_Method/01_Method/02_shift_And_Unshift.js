// ===============================
// 3. unshift()
// ===============================
// unshift() adds a value at the START of the array

let fruits = ["apple", "banana"];
fruits.unshift("orange");

console.log(fruits);
// ["orange","apple", "banana" ]

// ===============================
// 4. shift()
// ===============================
// shift() removes the FIRST value from the array
let foods = ["Rice", "Milk", "bread"];
foods.shift();

console.log(foods);
// [ "Milk" , "bread"]
